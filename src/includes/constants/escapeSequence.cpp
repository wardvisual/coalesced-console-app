#ifndef CPP_ESCAPE_SEQUENCE_CONSTANT
#define CPP_ESCAPE_SEQUENCE_CONSTANT

/* Moves the active position to the initial position of the current line. */
/* Carriage return means to return to the beginning of the current line without
  advancing downward. The name comes from a printer's carriage, as monitors
  were rare when the name was coined. This is commonly escaped as \r ,
  abbreviated CR, and has ASCII value 13 or 0x0D . */
#define CURRIAGE_RETURN 13

#endif